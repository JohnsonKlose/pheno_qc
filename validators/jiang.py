def make_in_interval(lower, upper):
    """
    函数构造器，生成一个闭包，判断数值是否在指定区间内。
    """
    # 构造上界判断函数
    if lower is None and upper is not None:
        def is_upper_bound(feature_key, final_value, payload):
            try:
                x = float(final_value)
            except (TypeError, ValueError):
                return False
            return x <= upper
        return is_upper_bound

    # 构造下界判断函数
    if upper is None and lower is not None:
        def is_lower_bound(feature_key, final_value, payload):
            try:
                x = float(final_value)
            except (TypeError, ValueError):
                return False
            return x >= lower
        return is_lower_bound

    # 构造无界实数判断函数
    if lower is None and upper is None:
        def is_any(feature_key, final_value, payload):
            try:
                float(final_value)
            except (TypeError, ValueError):
                return False
            return True
        return is_any

    # 构造区间判断函数
    if lower > upper:
        lower, upper = upper, lower
    def is_between(feature_key, final_value, payload):
        try:
            x = float(final_value)
        except (TypeError, ValueError):
            return False
        return lower <= x <= upper
    return is_between

